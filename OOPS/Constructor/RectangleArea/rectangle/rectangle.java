package JAVA.OOPS.Constructor.RectangleArea.rectangle;

public class rectangle {
    private int length;
    private int width;

    public void setLength(int length) {
        this.length = length;
    }
    public int getLength() {
        return length;
    }

    public void setWidth(int width) {
        this.width = width;
    }
    public int getWidth() {
        return width;
    }
}
